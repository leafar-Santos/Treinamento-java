package conteudo1a5;

public class _01_DeclaracaoVariaviesInt {
    public static void main(String[] args) {

        //Declaraçao de variáveis do tipo int
        int num1 = 0;
        int num2 = 0;
        int resultado;

        //Atribuição devaores às variáveis
        num1 = 10;
        num2 = 20;
        resultado = num1 + num2;

        //Exibição de dados no console
        System.out.println(resultado);
    }
}